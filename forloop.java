package GTMBatch50;

public class forloop {

	public static void main(String[] args) {
     for(int i=10; i<=40;i++)
     {
    	System.out.println(i); 
     }
     for(int i=-10; i>=-40;i--)
     {
     	System.out.println(i); 

     }
	     for(int i=-10; i<=10;i++)
	     {
	      	System.out.println(i); 

	     }

	}

}
